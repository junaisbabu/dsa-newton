package OwnEffect;

public class StringCompareToExample {
    public static void main(String[] args) {
        String s1 = "Bpple";
        String s2 = "Aanana";
        String s3 = "Cat";
        String s4 = "Dog";

        System.out.println("s1 - s2 : " + s1.compareTo(s2));

        System.out.println("s2 - s3 : " + s2.compareTo(s3));

        System.out.println("s3 - s4 : " + s3.compareTo(s4));
    }
}
